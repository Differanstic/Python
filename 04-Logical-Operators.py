'''
Logical Operators in Python
Logical operators are used to combine conditional statements.
They return a boolean value based on the logical operation performed.
And, Or, and Not are the three logical operators in Python.
'''
x = 3
y = 2
a = x > y
print(a) 

a = True
b = False

c = a & b
print("A and B:", c)

c = a | b
print("A or B:", c)

c =  not a
print("not A:", c)


